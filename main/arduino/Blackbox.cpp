const int pingMessage[8] = {1, 1, 1, 1, 0, 0, 0, 0};

Blackbox::Blackbox(int dataOut, int dataIn, int statusPin) {  
  Serial.begin(9600);
  pinMode(dataOut, OUTPUT);
  pinMode(dataIn, INPUT);
  pinMode(statusPin, OUTPUT);
  _dataOut = dataOut;
  _dataIn = dataIn;
  _statusPin = statusPin;
  _connectionStatus = false;
  _clientConnected = false;

  digitalWrite(_statusPin, HIGH);

  display = Adafruit_SSD1306(128, 64, &Wire, -1);
  if(!display.begin(SSD1306_SWITCHCAPVCC, 0x3C)) { 
    for(;;); // Don't proceed, loop forever
  }

  // Display setup
  display.setRotation(2);
  display.setTextSize(1);
  display.setTextColor(SSD1306_WHITE);

  // Splash
  display.clearDisplay();
  display.drawBitmap(0, 0, logo, 128, 64, WHITE);
  display.display();

  WiFi.softAP("BLACKBOX", "blackbox");
  wifiServer = WiFiServer(80);
  wifiServer.begin();

  digitalWrite(_statusPin, LOW);
    
  delay(1000);   
}

void Blackbox::update() {
  // Every second without delaying
  if (millis() % 1000 == 0) {
    Blackbox::updateScreen();
  }
  if (millis() % 500 == 0 && !_connectionStatus) {
    int buffer[1024];
    Blackbox::ping();
    Blackbox::receive(buffer, 1024, 500);
    if (Blackbox::isMessage(buffer, 1024, (int*)pingMessage, 8)) {
      _connectionStatus = true;
    }
  }

  Blackbox::updateWiFi();
}

void Blackbox::updateScreen() {
  display.clearDisplay();

  display.drawBitmap(0, -24, logo, 128, 64, WHITE);
  // Display IP Address and connection status
  display.setCursor(0,24);
  display.print(F("IP: "));
  display.println(WiFi.softAPIP());
  display.print(F("Connected: "));
  if (_connectionStatus) {
    display.println("Yes");
  } else {
    display.println("No");
  }

  if (!_clientConnected) {
    display.drawBitmap(111, 47, notconnected, 16, 16, WHITE);
  }
  
  display.display();
}

void Blackbox::updateWiFi() {
  if (client) {
    _clientConnected = true;
    if (client.connected()) {
      while (client.available()>0) {
        char c = client.read();
        Serial.println(c);
      }
    } else {
      client.stop();
      Serial.println("Client disconnected");
      _clientConnected = false;
    }
  } else {
    client = wifiServer.accept();
  }

  if (WiFi.softAPgetStationNum() == 0) {
    _clientConnected = false;
  }
  
  
}


// Connection ping
void Blackbox::ping() {
  Blackbox::send((int*)pingMessage, 8);
}

void Blackbox::receive(int* buffer, int size, int timeout) {
  int lastMillis = millis();
  int startPulse = 0;
  int pulseTime = 0;
  int i = 0;
  for (;;) {
    // Timeout
    if (millis() - lastMillis >= timeout) {
      Serial.println("Timed out.");
      buffer[i] = -1;
      break;
    }
    // Pulse high (start)
    if (digitalRead(_dataIn) && startPulse == 0) {
      // Reset timeout
      lastMillis = millis();
      // Set start of pulse
      startPulse = millis();
    // If startPulse was already set and pin is LOW
    } else if (startPulse != 0 && !digitalRead(_dataIn)) {
      // Calculate pulse time
      pulseTime = millis() - startPulse;
      // Reset pulse start time
      startPulse = 0;
      if (pulseTime == 10) {
        buffer[i] = 0;
      } else if (pulseTime == 20) {
        buffer[i] = 1;
      }

      i++;
      if (i >= size - 1) {
        buffer[size - 1] = -1;
        break;
      }
    }
  }
}

// Will only check from the beginning
bool Blackbox::isMessage(int* buffer, int bufferSize, int* check, int checkSize) {
  for (int i = 0; i < checkSize; i++) {
    if (buffer[i] != check[i]) {
      return false;
    }
  }
  return true;
}

void Blackbox::send(int* message, int length) {
  for (int i = 0; i < length; i++) {
    if (message[i] == 0) {
      pulse(10);
    } else if (message[i] == 1) {
      pulse(20);
    }
  }
}

void Blackbox::pulse(int ms) {
  digitalWrite(_dataOut, HIGH);
  digitalWrite(_statusPin, HIGH);
  delay(ms);
  digitalWrite(_dataOut, LOW);
  digitalWrite(_statusPin, LOW);
  delay(10); // Off delay will always be 10ms
}